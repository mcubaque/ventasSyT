function ConfirmEliminarProveedor() {
             //Ingresamos un mensaje a mostrar
            var mensaje = confirm("¿Estas seguro de querer eliminar el registro?");
            elemento = document.getElementById(eliminarProvee);
                       //Detectamos si el usuario acepto el mensaje
            if (mensaje) {
                    elemento.href=true;
                    
            }
            //Detectamos si el usuario denegó el mensaje
            else {
            alert("¡Acción cancelada!");
            }
            }

